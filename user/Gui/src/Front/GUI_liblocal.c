void GUI_local_ASC_data_obtain( GUI_front_t*pfront, uint16_t ASCcode )
{
	uint16_t offset;
	uint16_t nbytes;
	uint16_t Unitbytes;
	uint16_t start_asc;
	uint16_t start_end;

	GUI_FONT*pCUR;
	pCUR = ( GUI_FONT* )GUI_GetFront();
	pfront->Dbuf[0] =  0;
	while ( 1 )
	{
		if ( !pCUR )return;
		if ( pCUR->nbyte == 1 )
		{
			start_asc = pCUR->start_asc[0];
			start_end = pCUR->end_asc[0];
		}
		else if ( pCUR->nbyte == 2 )
		{
			start_asc = GET_HZ_ASCCODE( pCUR->start_asc );
			start_end = GET_HZ_ASCCODE( pCUR->end_asc );
		}
		if ( ASCcode >= start_asc&&ASCcode<=start_end )
		{
			pfront->way    =  pCUR->way;
			pfront->nbyte  =  pCUR->nbyte;
			pfront->wide   =  pCUR->wide;
			pfront->high   =  pCUR->high;
			pfront->CHwide =  pCUR->CHwide;
			offset 		   =  ASCcode - start_asc;
			Unitbytes      = ( pCUR->wide / 8 ) * pCUR->high;
			nbytes         =  offset * Unitbytes;
			GUI_memcpy(( void* )pfront->Dbuf, (( char* )pCUR->pcode + nbytes ), Unitbytes );
			return;
		}
		pCUR = ( GUI_FONT* )pCUR->pNext;
	}
}
